document.getElementById("on").onclick = function(){
    if(document.getElementById("pantallareves").className == "movimentreves"){
        document.getElementById("pantallareves").className = "movimentfinalreves";
    }else{
        document.getElementById("pantallareves").className= "movimentreves";
    }
    if(document.getElementById("pantalla").className == "moviment"){
        
        document.getElementById("pantalla").className = "movimentfinal";
    }else{
        
        document.getElementById("pantalla").className= "moviment";
    }
    }








    
